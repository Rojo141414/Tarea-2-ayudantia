// Función que recibe un string y retorna el string en mayúsculas
function convertirMayusculas(str) {
    return str.toUpperCase();
  }
  console.log("Minuscula a mayuscula")
  console.log(convertirMayusculas("hola")); 
  
  // Función que recibe un string y retorna el string en minúsculas
  function convertirMinusculas(str) {
    return str.toLowerCase();
  }
  console.log("Mayuscula a minuscula")
  console.log(convertirMinusculas("HOLA")); 
  
  
  function restar(numero1, numero2) {
    return numero1 - numero2;
  }
  console.log("Restar=")
  console.log(restar(10, 5)); 
  

  function dividir(numero1, numero2) {
    return numero1 / numero2;
  }
  console.log("Dividir=")
  console.log(dividir(10, 2));   

  function multiplicar(numero1, numero2) {
    return numero1 * numero2;
  }
  console.log("Multiplicar=")
  console.log(multiplicar(10, 2)); 
  
